
    public class Person {
        private String name;
        private int age;

        public String getName() {
            return name;
        }
        public String setName(String name) {
            this.name = name;
            return name;
        }

        public int getAge() {
            return age;
        }
        public int setAge(int age) {
            this.age = age;
            return age;
        }

}
