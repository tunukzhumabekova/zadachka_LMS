public class Car {

        private String name;
        private String brand;
        private int number;
        private int speed;

        public String getName() {
            return name;
        }
        public void setName(String name) {
            this.name = name;
        }

        public String getBrand() {
            return brand;
        }
        public String setBrand(String brand) {
            this.brand = brand;
            return brand;
        }

        public int getNumber() {
            return number;
        }
        public int setNumber(int number) {
            this.number = number;
            return number;
        }

        public int getSpeed() {
            return speed;
        }
        public int setSpeed(int speed) {
            this.speed = speed;
            return speed;
        }
    }

