public class School {

        private int numberSchool;
        private int quantityStudents;

        public int getNumberSchool() {
            return numberSchool;
        }
        public int setNumberSchool(int numberSchool) {
            this.numberSchool = numberSchool;
            return numberSchool;
        }

        public int getQuantityStudents() {
            return quantityStudents;
        }
        public int setQuantityStudents(int quantityStudents) {
            this.quantityStudents = quantityStudents;
            return quantityStudents;
        }
    }

